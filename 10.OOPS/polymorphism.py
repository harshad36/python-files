'''
Polymorphism
==============

Poly => Many
Morphism => Forms

A person plays defferent or many [poly] roles [Forms] in his life.
This is called as polymorphism in real life.

'''

a=10
b=20
c=a+b   # When operand beside + are numbers then + is arithmetic operator.
print("Addition is:",c)


x="Itvedant"
y="Eclass"

z=x+y  #When operand beside + are string then + is concatenation operator
print("Value of z id:",z)

'''
In above program operator + is used as arithmetic operator
when operands are integer, + is used as concatenation operator when operand are string

Thus single operator playing two different role, i.e arithmetic and
concatenation operator based on operands.

Single operator + has two forms. Thus is called as operator overloading
which means to achieve polymorphism.
'''
